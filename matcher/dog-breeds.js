export const DOG_BREEDS = Object.freeze([...new Set(BREEDS)].sort((a, b) => a.localeCompare(b, 'ru')));
export const DOG_BREED_OPTIONS = Object.freeze(['Метис', 'Не знаю', ...DOG_BREEDS]);

const normalized = (value) => String(value || '').trim().toLocaleLowerCase('ru-RU').replaceAll('ё', 'е');

export function searchDogBreeds(query = '') {
  const term = normalized(query);
  if (!term) return DOG_BREED_OPTIONS;
  const starts = [];
  const contains = [];
  for (const label of DOG_BREED_OPTIONS) {
    const candidate = normalized(label);
    if (candidate.startsWith(term)) starts.push(label);
    else if (candidate.includes(term)) contains.push(label);
  }
  return [...starts, ...contains];
}

export function normalizeDogBreed(value) {
  const raw = String(value || '').trim();
  if (raw === 'MIXED') return 'Метис';
  if (raw === 'UNKNOWN') return 'Не знаю';
  return raw;
}

export function isKnownDogBreed(value) {
  const target = normalized(normalizeDogBreed(value));
  return DOG_BREED_OPTIONS.some((label) => normalized(label) === target);
}
